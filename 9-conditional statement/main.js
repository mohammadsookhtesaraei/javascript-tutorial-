

// conditional statement


// if  and // else

const number=5;
if(number > 3) {
    console.log(`number greater than three`)
} else {
    console.log("another")
};


// else if

const num=10;

if(num < 8) {
    console.log("num smaller than ten")
} else if (num > 8) {
    console.log("num greater than eight")
} else {
    console.log("OTHER")
};


// ternary operator

num > 8 ? console.log("num greater than eight") : console.log("num smaller than eight")


